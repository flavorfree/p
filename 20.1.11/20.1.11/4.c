#define _CRT_SECURE_NO_WARNINGS 1

//输入密码，登陆三次，超过三次退出

#include<stdio.h>
#include<stdlib.h>
#include<string.h>

int main()
{
	int i = 0;
	char password[20] = { 0 };

	for (i = 0; i < 3; i++)
	{
		printf("请输入密码；>");
		scanf("%s", &password);
		if (strcmp(password, "123456") == 0)//string compar字符串比较
		{
			printf("登录成功\n");
			break;
		}
		else
		{
			printf("密码错误，重新输入！\n");
		}
	}
	if (i == 3)
		printf("三次密码都错，退出程序!\n");

	system("pause");
	return 0;
}